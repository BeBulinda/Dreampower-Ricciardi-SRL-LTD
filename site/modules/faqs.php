                        <h3 class="topmargin_0">1. Why LED’s?</h3>
                        <p>As a rule, LED bulbs use 80% to 90% less electricity 
                            than standard bulbs. They have an unparalleled even 
                            spectrum of light and have a lifespan beyond ten years. 
                            LED’s provide us the most efficient way to save energy and 
                            conserve our natural resources. If LED’s were implemented right 
                            now universally, we would not need to build another power plant. 
                            LEDs would actually eliminate the need for over 30 existing power plants.</p>
