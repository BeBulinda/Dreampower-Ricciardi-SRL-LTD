                        <h3 class="topmargin_0">7. How long do LEDs for general lighting really last?</h3>
                        <p>Remember, the longevity of LED can be 100,000 hours. LED’s 
                            are solid state devices (SSL- or “solid state lighting”); 
                            they will not burn out. Life expectancy for SSL lighting 
                            is upward of 50,000 to 100,000 hours or more which means 
                            no maintenance costs for facilities workers to replace lights. 
                            Over time, the cost of the fluorescent or incandescent bulbs 
                            themselves can add up to a significant savings when converting to LED’s.</p>
