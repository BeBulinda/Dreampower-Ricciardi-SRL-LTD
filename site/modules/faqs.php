                        <h3 class="topmargin_0">5. Why are LED lights more expensive?</h3>
                        <p>LED light bulbs use an actual circuit board to operate 
                            and are made of electronic components. Essentially, 
                            the could be considered an electronic device. This 
                            technology continues to gain advantages almost daily. 
                            The manufacturing and supply/ demand of general lighting 
                            products are gearing up today, and we will see costs continue 
                            to decline as the adoption rate of LED Lighting increases.</p>
                    </div>
                    <div class="tab-pane fade" id="vertical-tab6">
                        <h3 class="topmargin_0">6. Is LED light a different type of light?</h3>
                        <p>Yes, LED light is said to be safer, healthier light. 
                            LEDs do NOT produce any sort of ultraviolet radiation 
                            which causes fabric fading, color fading in Art, carpeting 
                            and other soft goods. There is none of the ‘buzzing’ or 
                            ‘flickering’ that many people are sensitive to with LED 
                            Lights. Residential and especially commercial and industrial 
                            plants, stores libraries, galleries, and water houses can 
                            immediately benefit from LED Lighting.</p>
                    </div>
