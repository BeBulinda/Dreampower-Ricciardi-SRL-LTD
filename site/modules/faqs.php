                        <h3 class="topmargin_0">2. Do LED light bulbs contain mercury?</h3>
                        <p>No. LED bulbs do not contain mercury. They can actually 
                            be recycled as they do not contain hazardous substances 
                            and are manufactured without hazardous substances.</p>
                    </div>
                    <div class="tab-pane fade" id="vertical-tab3">
                        <h3 class="topmargin_0">3. How do LED light bulbs compare to CFL bulbs?</h3>
                        <p>Studies show LED bulbs use 50% less energy than CFL bulbs 
                            and in many cases last 10 times longer than CFL light bulbs. 
                            They are much more durable, environmentally friendly, vibrant 
                            and shock resistant and offer excellent light quality, both 
                            indoor and outdoor.</p>
                    </div>
                    <div class="tab-pane fade" id="vertical-tab4">
                        <h3 class="topmargin_0">4. Do LED bulbs produce as much heat as CFL or Incandescent bulbs?</h3>
                        <p>LED light bulbs emit much less heat than a CFL or incandescent 
                            bulb. In many cases, you can actually feel the temperature 
                            difference just by being near the light. LED bulb lights 
                            will always operate at a lower temperature than a CFL or 
                            incandescent which has immediate benefits in reduced 
                            cooling bills in the summer months where we are paying 
                            for our Air Conditioning to cool our offices.</p>
                    </div>
