                            <p>
                                Going green doesn’t mean you need to freeze during 
                                the winter or stay in the darkness throughout the night. 
                                We can introduce lots of small and easy changes in our 
                                everyday life that will make the difference. It’s not 
                                difficult to choose the CFL instead of the incandescent. 
                                It’s not expensive to buy some solar lights for your garden.
                            </p>
                            <p>
                                Some actions might be that simple that we don’t 
                                even notice. However, they DO make the change. A 
                                solar powered calculator for instance, won’t save 
                                that much energy, but it will cultivate the green 
                                way of thinking to the children! Well, that is an 
                                accomplishment.
                            </p>

                            <blockquote class="topmargin_60 bottommargin_60">
                                Going green is simple and easy.
                                It only depends on the way we think!
                                <div class="blockquote-meta">
                                    <h5>Carmelo Ricciardi</h5>
                                    <p>Director</p>
                                </div>

                            </blockquote>
                            <h3 class="entry-title topmargin_10">
                                <a href="#" rel="bookmark">Reduce energy consumption</a>
                            </h3>
                            <ol>
                                <li>
                                    Buy energy efficient appliances: They will use 
                                    a lot less energy and consumables (like water and washing powder). 
                                    Energy efficient light bulbs have already become a must…
                                    Insulate your home: It will lower the heating and cooling needs.
                                </li>

                                <li>
                                    Always full up the washer: Do not waste water and 
                                    electricity to run a dish or clothe washer when it 
                                    is almost empty. Lower temperatures will save you 
                                    energy as well.
                                </li>
                                <li>
                                    Close the fridge door: Don’t leave heat go away. Going green at home is that easy!
                                </li>
                                <li>
                                    Don’t leave electronics on stand-by mode: They 
                                    will not stop using electricity. It is better to 
                                    completely switch off all appliances. A plug-board 
                                    with a switch will do the job.
                                </li>
                                <li>
                                    Turn off the lights: When you leave a room, 
                                    you don’t need any light there.
                                </li>
                            </ol>

                            <h3 class="entry-title topmargin_10">
                                <a href="#" rel="bookmark">Choose your shopping wisely</a>
                            </h3>
                            <ol>
                                <li>
                                    Buy recycled products: To reduce the natural 
                                    resources used all over the world.
                                </li>

                                <li>
                                    Don’t buy over-packed products: That not only 
                                    minimizes our impact on nature, but it reduces 
                                    the waste produced too.
                                </li>
                                <li>
                                    Buy products in large (economic) packages: 5 bottles 
                                    of 1 litre produce lots more garbage than one bottle of 
                                    5 Litres. Isn’t that clear?
                                </li>
                                <li>
                                    Buy native products: If something is produced 
                                    somewhere nearby, then the CO2 emissions due to the 
                                    transportation will decrease significantly. Thus, 
                                    they will be cheaper too.
                                </li>
                                <li>
                                    Buy organic products: These food is healthier 
                                    because no toxic pesticides are used during its 
                                    production. As a result, these chemicals won’t 
                                    penetrate and pollute the underground aquifers.
                                </li>
                                <li>
                                    Prefer eco-friendly cleaning liquids: Biodegradable 
                                    cleaners don’t pollute the environment.
                                </li>
                                <li>
                                    Avoid Aerosols: They contain CFC gases which 
                                    are extremely dangerous for the ozone layer.
                                </li>
                                <li>
                                    Prefer Bio-plastics: If left out in nature, 
                                    they will degrade easily.
                                </li>
                            </ol>

                            <h3 class="entry-title topmargin_10">
                                <a href="#" rel="bookmark">Act Green</a>
                            </h3>
                            <ol>
                                <li>
                                    Dispose your waste properly: Batteries, 
                                    toxic fluids etc. must be handled in a specific way.
                                </li>

                                <li>
                                    Reduce Reuse Recycle: The 3Rs. Reducing and 
                                    Reusing the packages, containers and every kind 
                                    of product that is going to be disposed, minimizes 
                                    the trash produced. Recycling the garbage will 
                                    save the natural sources from exhaustion.
                                </li>
                                <li>
                                    Plant a tree: Lets Green- The-World. Trees 
                                    filter air and reduce CO2 in the atmosphere.
                                </li>
                                <li>
                                    Join a Group: You can either be an active member 
                                    in a group (cleaning beaches for example, like 
                                    these people do) or be a passive one (Join our group on Facebook).
                                </li>
                                <li>
                                    Notify the authorities: If you see something bad. 
                                    It might be a company polluting the environment 
                                    or a leak on an oil tank. Make sure that it will 
                                    be fixed as soon as possible. Also, if you know 
                                    of a trashy place, use our Green It! Project to 
                                    inform us. We will do the rest.
                                </li>
                            </ol>

                            <h3 class="entry-title topmargin_10">
                                <a href="#" rel="bookmark">Use Renewables</a>
                            </h3>
                            <ol>
                                <li>
                                    Install some photovoltaic panels: They will 
                                    produce free-green energy and the need no maintenance.
                                </li>

                                <li>
                                    Install a wind turbine: It will do the same thing. 
                                    Where on earth doesn’t the wind blow?
                                </li>
                                <li>
                                    What about a solar water heater: You just place 
                                    it on your roof and it heats up the water. It’s 
                                    that simple. More residentials solar energy ideas.
                                </li>
                            </ol>
